

public class Sprite
{

    public Sprite(int x, int y, int img, int col, int bits)
    {
        this.x = x;
        this.y = y;
        this.img = img;
        this.col = col;
        this.bits = bits;
    }

    public int x;
    public int y;
    public int img;
    public int col;
    public int bits;
}